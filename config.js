/* ============================================================
   Supabase 設定
   ------------------------------------------------------------
   ここに Supabase プロジェクトの URL と anon キーを入れると
   クラウド同期(複数端末でのデータ共有)が有効になります。
   空のままなら localStorage のみで動作します。

   ※ anon キーはクライアントに公開される前提の「公開可能キー」です。
     データは RLS(行レベルセキュリティ)で各ユーザー自身の行のみに
     制限されるため、公開リポジトリに置いても安全です。
     （絶対に service_role キーは置かないこと）
   ============================================================ */
window.JANSOU_CONFIG = {
  SUPABASE_URL: "",
  SUPABASE_ANON_KEY: "",

  /* LINEミニアプリ / LIFFアプリのID。
     LINE Developers Console で発行した LIFF ID を入れると、
     LINE内で開いたときに LIFF として初期化されます。
     空ならLIFFは無効(通常のWebアプリとして動作)。 */
  LIFF_ID: "",
};
